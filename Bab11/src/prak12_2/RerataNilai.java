public class RerataNilai {
    
    
    
    public int average(int x, int y){
        int total;
        total = (x + y) / 2;
        return total;
    }
    
    public double average(double x, double y){
        double total;
        total = (x + y) / 2;
        return total;
    }
    
    public int average(int x, int y, int z){
        int total;
        total = (x + y + z) / 3;
        return total;
    }
}
